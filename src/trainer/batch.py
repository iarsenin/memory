"""
Phase 5 — Batch Generator.

Builds a training batch for one sleep cycle from:
  • New (un-consolidated) memories for the current 3-day window
  • Replay sample from previously consolidated memories
  • Optional regularizer exchanges to preserve assistant formatting

Returned format: list of HuggingFace chat-format message lists
  [ [{"role": "user", "content": "..."}, {"role": "assistant", "content": "..."}], ... ]

Mixture ratios (applied per new memory item):
  40% declarative  — flat "Remember: Subject predicate value." exchange
  40% QA           — question/answer filled strictly from train_config qa_train_templates
  20% dialogue     — 2–3 turn snippet from the raw dialogue log proving the fact

Replay buffer:
  Weighted random sample from all consolidated memories.
  Weight = salience_score × temporal_decay (both stored in memories JSONL from Phase 4).

Regularizer:
  10 generic Llama-3 assistant exchanges hardcoded here. These are drawn
  from the training distribution to prevent the model from collapsing into
  a pure fact-spitting format.

Test leakage prevention:
  QA templates come ONLY from train_config["qa_train_templates"]. This file
  is generated by the runner, never modified here. Phase 7 uses a completely
  separate eval template list defined in eval_config.json.
"""

from __future__ import annotations

import json
import random
from typing import Any

# Reuse the same category mapper and token extractor used in Phase 3/4
# so that the replay-weighting and QA category labelling are consistent.
from ..salience.score import _map_category, _primary_tokens

# ---------------------------------------------------------------------------
# System prompts
# ---------------------------------------------------------------------------

_SYS_REMEMBER = (
    "You are a helpful AI assistant that remembers personal information "
    "about users. Retain the following fact accurately."
)
_SYS_RECALL = (
    "You are a helpful AI assistant with access to personal information "
    "about users. Answer questions based on what you know about them."
)

